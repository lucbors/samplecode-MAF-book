package com.tamcapp.mobilebook.mobile.model.places;

public class LatLng {
private double lat, lng;
    public LatLng() {
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLat() {
        return lat;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public double getLng() {
        return lng;
    }
}
